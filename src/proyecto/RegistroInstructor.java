    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField3.getText();
        
        if (name.length() < 3) {
            Handler.showMessage("Nombre muy corto", "Nombre invalido", Handler.ERROR);
            jTextField3.setText("");
            return;
        }
        
        String firstSurName = jTextField4.getText();
        if (firstSurName.length() < 3) {
            Handler.showMessage("Primer apellido muy corto", "Primer apellido invalido", Handler.ERROR);
            jTextField4.setText("");
            return;
        }
        
        String secondSurName = jTextField5.getText();
        if (secondSurName.length() < 3) {
            Handler.showMessage("Segundo apellido muy corto", "Segundo apellido invalido", Handler.ERROR);
            jTextField5.setText("");
            return;
        }
        
        
        boolean availability = jCheckBox1.isSelected();
        
        
        long identification;
        if (jTextField6.getText().length() < 8) {
            Handler.showMessage("Identificacion invalida", "Identificación invalida", Handler.ERROR);
            jTextField6.setText("");
            return;
        }
        try {
            identification = Long.parseLong(jTextField6.getText());
        } catch (NumberFormatException nfe) {
            Handler.showMessage("La identificación ingresada no es un número", "Número no valido", Handler.ERROR);
            jTextField6.setText("");
            return;
        }
        
        
        String speciality = jTextField7.getText();
        if (speciality.length() < 1) {
            Handler.showMessage("Identificacion invalida", "Identificación invalida", Handler.ERROR);
            jTextField7.setText("");
            return;
        }
        
        
        int experience;
        if (jTextField1.getText().length() < 1) {
            Handler.showMessage("Experiencia no valida", "Experiencia invalida", Handler.ERROR);
            jTextField1.setText("");
            return;
        }
        try {
            experience = Integer.parseInt(jTextField2.getText());
        } catch (NumberFormatException nfe) {
            Handler.showMessage("Experiencia ingresada no es un número", "Número no valido", Handler.ERROR);
            jTextField1.setText("");
            return;
        }
        
        
        
        
        long salary = Long.parseLong(jTextField2.getText());
        if (jTextField2.getText().length() < 1) {
            Handler.showMessage("Salario no valido", "salario invalido", Handler.ERROR);
            jTextField2.setText("");
            return;
        }
        
        try {
            salary = Long.parseLong(jTextField2.getText());
        } catch (NumberFormatException nfe) {
            Handler.showMessage("El salario ingresado no es un número", "Número no valido", Handler.ERROR);
            jTextField2.setText("");
            return;
        }
        
        
        instructors.add(new Instructor(name, firstSurName, secondSurName, identification, salary, experience, speciality, availability));
        this.clear();
    }//GEN-LAST:event_jButton1ActionPerformed

