    public void checkData() {
        String identification = "";
        do {
            identification = Handler.inputString("Digite la identificación del instructor").trim();
            if (identification == null) identification = "";
        } while (identification.length() < 8);
        
        Instructor instructor = instructors.find(Long.parseLong(identification));
        if (instructor == null) {
            Handler.showMessage("El cliente no existe", "Error: Not Found", Handler.ERROR);
            this.dispose();
            
            instructor = new Instructor("", "", "", 0L, 0L, 0, "", false);
        }
        
        
        jTextField3.setText(instructor.getName());
        jTextField4.setText(instructor.getFirstSurName());
        jTextField5.setText(instructor.getSecondSurName());
        jTextField6.setText(String.valueOf(instructor.getIdentification()));
        jCheckBox1.setSelected(instructor.isAvailability());
    }
      
